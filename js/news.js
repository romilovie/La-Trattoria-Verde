// Получение всех кнопок управления новостными карточками
document.querySelectorAll('.news-btn').forEach(button => {

    // -------------------------------------------------
    // НАЗНАЧЕНИЕ ОБРАБОТЧИКА СОБЫТИЯ НАЖАТИЯ
    // -------------------------------------------------
    button.addEventListener('click', () => {

        // Определение родительской карточки новости
        const card = button.closest('.news-card');

        // Переключение состояния карточки (открыта / закрыта)
        card.classList.toggle('open');

        // -------------------------------------------------
        // ИЗМЕНЕНИЕ ТЕКСТА КНОПКИ В ЗАВИСИМОСТИ ОТ СОСТОЯНИЯ
        // -------------------------------------------------
        button.textContent = card.classList.contains('open')
            ? 'Свернуть'
            : 'Подробнее';
    });
});

